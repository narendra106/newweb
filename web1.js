function showslide(){
    const inmenu =document.querySelector('.inmenu')
    inmenu.style.display='flex'
}
function closeslide(){
    const inmenu=document.querySelector('.inmenu')
    inmenu.style.display='none'
}
function showbox(){
    const inlogbox=document.querySelector('.inlogbox')
    inlogbox.style.display='flex'
}
function closebox(){
    const inlogbox=document.querySelector('.inlogbox')
    inlogbox.style.display='none'
}